// Math.cbrt方法用于计算一个数的立方根。
Math.cbrt(2)  // 1.2599210498948734

// ES5
parseInt('12.34') // 12
parseFloat('123.45#') // 123.45

// ES6

Number.parseInt('12.34') // 12
Number.parseFloat('123.45#') // 123.45


// Math.trunc方法用于去除一个数的小数部分，返回整数部分。
Math.trunc(4.6) // 4
Math.trunc(4.1) // 4

// Math.sign() 判断正数 +1 ，负数 -1 ，0 0， -0 -0 ，NaN
Math.sign(4) // +1
Math.sign(-2) // -1
Math.sign(-0) // -0
